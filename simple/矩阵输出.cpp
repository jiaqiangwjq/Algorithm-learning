/*
题目描述
输出以下4*5的矩阵

  1  2  3  4  5

  2  4  6  8 10

  3  6  9 12 15

  4  8 12 16 20

要求使用循环实现，注意每行输出5个数字，每个数字占3个字符的宽度，右对齐。

 

输入
无
输出
每行输出5个数字，每个数字占3个字符的宽度，右对齐。
 

样例输入
无
样例输出
  1  2  3  4  5
  2  4  6  8 10
  3  6  9 12 15
  4  8 12 16 20
  */

#include<stdio.h>
int main()
{
	//int j = 1;
	for (int i = 1; i <= 4; i++)
	{
		for (int j = 1; j <= 5; j++)
		{
			printf("%3d", i*j);
		}
		printf("\n");
	}
	return 0;
}